/**
 * Shared GLSL utility functions injected into all fragment shaders.
 *
 * These are pure math functions with no side effects.
 * All use GLSL ES 3.00 syntax.
 */
export const GLSL_COMMON = /* glsl */ `
// --- Hash / noise ---

// High-quality hash from Dave Hoskins
// https://www.shadertoy.com/view/4djSRW
float hash11(float p) {
  p = fract(p * 0.1031);
  p *= p + 33.33;
  p *= p + p;
  return fract(p);
}

float hash21(vec2 p) {
  vec3 p3 = fract(vec3(p.xyx) * 0.1031);
  p3 += dot(p3, p3.yzx + 33.33);
  return fract((p3.x + p3.y) * p3.z);
}

vec2 hash22(vec2 p) {
  vec3 p3 = fract(vec3(p.xyx) * vec3(0.1031, 0.1030, 0.0973));
  p3 += dot(p3, p3.yzx + 33.33);
  return fract((p3.xx + p3.yz) * p3.zy);
}

// Value noise (smooth)
float vnoise(vec2 p) {
  vec2 i = floor(p);
  vec2 f = fract(p);
  f = f * f * (3.0 - 2.0 * f); // smoothstep
  float a = hash21(i);
  float b = hash21(i + vec2(1.0, 0.0));
  float c = hash21(i + vec2(0.0, 1.0));
  float d = hash21(i + vec2(1.0, 1.0));
  return mix(mix(a, b, f.x), mix(c, d, f.x), f.y);
}

// --- SDF shapes ---

float sdCircle(vec2 p, float r) {
  return length(p) - r;
}

float sdBox(vec2 p, vec2 b) {
  vec2 d = abs(p) - b;
  return length(max(d, 0.0)) + min(max(d.x, d.y), 0.0);
}

float sdCross(vec2 p, float size, float thickness) {
  float h = sdBox(p, vec2(size, thickness));
  float v = sdBox(p, vec2(thickness, size));
  return min(h, v);
}

// --- Halftone ---

// Returns dot coverage [0,1] for a halftone screen
// uv: screen UV (0-1), gridSize: dot spacing in UV space, value: brightness
float halftone(vec2 uv, float gridSize, float value) {
  vec2 cell = fract(uv / gridSize) - 0.5;
  float d = length(cell);
  float r = sqrt(value) * 0.5; // dot radius proportional to sqrt of value (area-based)
  return smoothstep(r + 0.01, r - 0.01, d);
}

// --- Color utilities ---

vec3 rgb2hsv(vec3 c) {
  vec4 K = vec4(0.0, -1.0/3.0, 2.0/3.0, -1.0);
  vec4 p = mix(vec4(c.bg, K.wz), vec4(c.gb, K.xy), step(c.b, c.g));
  vec4 q = mix(vec4(p.xyw, c.r), vec4(c.r, p.yzx), step(p.x, c.r));
  float d = q.x - min(q.w, q.y);
  float e = 1.0e-10;
  return vec3(abs(q.z + (q.w - q.y) / (6.0 * d + e)), d / (q.x + e), q.x);
}

vec3 hsv2rgb(vec3 c) {
  vec4 K = vec4(1.0, 2.0/3.0, 1.0/3.0, 3.0);
  vec3 p = abs(fract(c.xxx + K.xyz) * 6.0 - K.www);
  return c.z * mix(K.xxx, clamp(p - K.xxx, 0.0, 1.0), c.y);
}
`;
